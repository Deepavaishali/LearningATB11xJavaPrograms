package ex_07_increment_decrement_operator;



public class LAB078_PRE_DEC {
    public static void main(String[] args) {
        int a =10;
        int result =--a;
        System.out.println(result);
        System.out.println(a);

        //line no | a| result
        //5 | 10 |NA
        //6 | 9 |9
    }

}
