package ex_02_java_basic_part2;

public class Lab011_constants {
    public static void main(String[] args) {
        int a=10;
        a=20;
        //if you use final variable, you cannot change the value
        final int b=10;
        //now you cannot change the value
        System.out.println(a);
        System.out.println(b);
        //constants is something, we cannot change the value
        //for example, pi =3.14 which is constant
       final float p = 3.13f;
        //you cannot change like this

    }
}
