package ex_04_operators;

public class LAB036_logical_opertor {
    public static void main(String[] args) {
        boolean a = true;
        System.out.println(!a);//!a means not of a
        //means if a is true, it prints not to true one, which is false
        boolean b = true;
        System.out.println(!!b); //mean not of not of b
        // result is true
        boolean c = true || false;
                // which explains the OR gate
        // RESULT IS TRUE
    }

}
