package ex_01_Java_Basic;

public class LAB008_math_operations {
    public static void main(String[] args) {
        System.out.println("2+2");
        //whatever present inside double quotes, it will print the same statement
        System.out.println(2+2);
        //here, it will do the math operation
        System.out.println(2/2);
        System.out.println(1/2);
        System.out.println(3/2);
        /*here, output is 1.5, but it prints only interger part.
        because println says, it prints only interger part, not the decimal one*/
        System.out.println(3*2);
        System.out.println(3-2);
    }
}
